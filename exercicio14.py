# Faça um programa que calcule o volume de um paralelepípedo. Onde você informará o comprimento, a largura e a altura. 

# Fórmula: V = c x l x h

# Onde: V = Volume

# c = comprimento

# l = largura

# h = altura

# Escreva seu código aqui#

c = int(input( "Digite o comprimento do paralelepípedo: "))

l = int(input( "Digite a largura do paralelepípedo: "))

h = int(input( "Digite a altura do paralelepípedo: "))

V = c * l * h

print(V)